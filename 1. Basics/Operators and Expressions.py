 # Operators and Expressions in Python

# 1. Arithmetic Operators
num1 = 10
num2 = 5

# Addition
result_add = num1 + num2
print("Addition:", result_add)

# Subtraction
result_subtract = num1 - num2
print("Subtraction:", result_subtract)

# Multiplication
result_multiply = num1 * num2
print("Multiplication:", result_multiply)

# Division
result_divide = num1 / num2
print("Division:", result_divide)

# Modulus (remainder)
result_modulus = num1 % num2
print("Modulus:", result_modulus)

# 2. Comparison Operators
# Equal to
is_equal = num1 == num2
print("\nIs Equal?", is_equal)

# Not equal to
is_not_equal = num1 != num2
print("Is Not Equal?", is_not_equal)

# Greater than
is_greater_than = num1 > num2
print("Is Greater Than?", is_greater_than)

# Less than
is_less_than = num1 < num2
print("Is Less Than?", is_less_than)

# 3. Logical Operators
# AND
is_and = (num1 > 0) and (num2 > 0)
print("\nIs 'num1' and 'num2' both greater than 0?", is_and)

# OR
is_or = (num1 > 0) or (num2 > 0)
print("Is 'num1' or 'num2' greater than 0?", is_or)

# NOT
is_not = not (num1 > 0)
print("Is 'num1' not greater than 0?", is_not)
