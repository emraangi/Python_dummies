# Variables and Data Types in Python

# 1. Integer
age = 25

# 2. Float
height = 1.75

# 3. String
name = "John"

# 4. Boolean
is_student = True

# Displaying variable values
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student?", is_student)

# Checking Data Types
print("\nData Types:")
print("Type of 'name':", type(name))
print("Type of 'age':", type(age))
print("Type of 'height':", type(height))
print("Type of 'is_student':", type(is_student))

# Type Conversion
print("\nType Conversion:")
age_str = str(age)
height_int = int(height)
is_student_str = str(is_student)

print("Converted 'age' to string:", age_str)
print("Converted 'height' to integer:", height_int)
print("Converted 'is_student' to string:", is_student_str)
